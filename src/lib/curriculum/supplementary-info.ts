export function getTipsByCategory(category: string) {
  return supplementaryTips.filter((t) => t.category === category);
}

export function getQuickReferencesByCategory(category: string) {
  return quickReferences.filter((q) => q.category === category);
}

export function getTipCategories() {
  return Array.from(new Set(supplementaryTips.map((t) => t.category)));
}

export function getQuickReferenceCategories() {
  return Array.from(new Set(quickReferences.map((q) => q.category)));
}
