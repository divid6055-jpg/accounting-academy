export function getAdditionalTermsByCategory(category: string) {
  return additionalTerms.filter((t) => t.category === category);
}

export function searchAdditionalTerms(query: string) {
  return additionalTerms.filter(
    (t) =>
      t.term.includes(query) ||
      t.english.toLowerCase().includes(query.toLowerCase()) ||
      t.definition.includes(query)
  );
}

export function getAdditionalTermCategories() {
  return Array.from(new Set(additionalTerms.map((t) => t.category)));
}
