export function getFAQsByCategory(category: string) {
  return faqDatabase.filter((f) => f.category === category);
}

export function getFAQsByDifficulty(difficulty: string) {
  return faqDatabase.filter((f) => f.difficulty === difficulty);
}

export function searchFAQs(query: string) {
  return faqDatabase.filter(
    (f) => f.question.includes(query) || f.answer.includes(query) || f.detailedAnswer.includes(query)
  );
}

export function getFAQById(id: string) {
  return faqDatabase.find((f) => f.id === id);
}

export function getRelatedFAQs(id: string) {
  const faq = faqDatabase.find((f) => f.id === id);
  if (!faq || !faq.relatedQuestions) return [];
  return faq.relatedQuestions
    .map((rid) => faqDatabase.find((f) => f.id === rid))
    .filter(Boolean);
}
