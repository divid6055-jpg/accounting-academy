export function getAdvicesByCategory(category: string) {
  return accountingAdvices.filter((a) => a.category === category);
}

export function getProblemCategories() {
  return Array.from(new Set(accountingProblems.map((p) => p.category)));
}

export function getAdviceCategories() {
  return Array.from(new Set(accountingAdvices.map((a) => a.category)));
}

export function searchProblems(query: string) {
  const q = query.toLowerCase();
  return accountingProblems.filter(
    (p) =>
      p.title.includes(query) ||
      p.description.includes(query) ||
      p.symptoms.some((s) => s.includes(query))
  );
}
