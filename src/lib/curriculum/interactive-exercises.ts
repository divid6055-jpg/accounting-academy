export function getExercisesByCategory(category: string) {
  return interactiveExercises.filter((e) => e.category === category);
}

export function getExercisesByDifficulty(difficulty: string) {
  return interactiveExercises.filter((e) => e.difficulty === difficulty);
}

export function getExerciseCategories() {
  return Array.from(new Set(interactiveExercises.map((e) => e.category)));
}

export function searchInteractiveExercises(query: string) {
  return interactiveExercises.filter(
    (e) =>
      e.title.includes(query) ||
      e.description.includes(query) ||
      e.tags.some((t) => t.includes(query))
  );
}
