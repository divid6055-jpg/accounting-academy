export function getExamplesByCategory(category: string) {
  return accountingExamples.filter((e) => e.category === category);
}

export function getExamplesByDifficulty(difficulty: string) {
  return accountingExamples.filter((e) => e.difficulty === difficulty);
}

export function getExampleCategories() {
  return Array.from(new Set(accountingExamples.map((e) => e.category)));
}

export function searchExamples(query: string) {
  const q = query.toLowerCase();
  return accountingExamples.filter(
    (e) =>
      e.title.includes(query) ||
      e.scenario.includes(query) ||
      e.category.includes(query)
  );
}
