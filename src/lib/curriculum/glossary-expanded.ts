export function searchExpandedGlossary(query: string, category?: string) {
  const q = query.trim().toLowerCase();
  return glossaryExpanded.filter((term) => {
    const matchesQuery =
      !q ||
      term.term.includes(query) ||
      term.english.toLowerCase().includes(q) ||
      term.definition.includes(query) ||
      term.detailedDefinition.includes(query);
    const matchesCategory = !category || category === "الكل" || term.category === category;
    return matchesQuery && matchesCategory;
  });
}

export function getExpandedGlossaryCategories() {
  const cats = new Set(glossaryExpanded.map((g) => g.category));
  return ["الكل", ...Array.from(cats)];
}

export function getExpandedGlossaryByCategory(category: string) {
  return glossaryExpanded.filter((g) => g.category === category);
}
