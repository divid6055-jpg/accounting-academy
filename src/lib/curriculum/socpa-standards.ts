export function getSOCPAStandardByCode(code: string) {
  return socpaStandards.find((s) => s.code === code);
}

export function getSOCPAStandardsByCategory(category: string) {
  return socpaStandards.filter((s) => s.category === category);
}

export function getTaxLawByName(name: string) {
  return saudiTaxLaws.find((t) => t.name === name);
}

export function getTaxLawsByType(type: string) {
  return saudiTaxLaws.filter((t) => t.lawType === type);
}

export function getCompanyTypeByName(name: string) {
  return saudiCompanyTypes.find((c) => c.name === name);
}

export function searchSOCPAStandards(query: string) {
  const q = query.toLowerCase();
  return socpaStandards.filter(
    (s) =>
      s.title.includes(query) ||
      s.englishTitle.toLowerCase().includes(q) ||
      s.objective.includes(query)
  );
}
