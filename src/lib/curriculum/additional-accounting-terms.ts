export function getAdditionalTermsByCategory(category: string) {
  return additionalAccountingTerms.filter((t) => t.category === category);
}

export function getAdditionalTermsBySubcategory(subcategory: string) {
  return additionalAccountingTerms.filter((t) => t.subcategory === subcategory);
}

export function getPracticalAdvicesByCategory(category: string) {
  return practicalAdvices.filter((a) => a.category === category);
}

export function searchAdditionalTerms(query: string) {
  return additionalAccountingTerms.filter(
    (t) => t.term.includes(query) || t.english.toLowerCase().includes(query.toLowerCase()) || t.definition.includes(query)
  );
}

export function searchPracticalAdvices(query: string) {
  return practicalAdvices.filter(
    (a) => a.title.includes(query) || a.advice.includes(query)
  );
}
