export function getQuestionsByCategory(category: string) {
  return accountingQuestions.filter((q) => q.category === category);
}

export function getQuestionsByDifficulty(difficulty: string) {
  return accountingQuestions.filter((q) => q.difficulty === difficulty);
}

export function searchQuestions(query: string) {
  return accountingQuestions.filter(
    (q) => q.question.includes(query) || q.explanation.includes(query)
  );
}

export function getCaseStudyById(id: string) {
  return miniCaseStudies.find((c) => c.id === id);
}
