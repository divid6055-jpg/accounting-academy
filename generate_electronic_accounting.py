def format_software(sw):
    """Format accounting software entry as TypeScript"""
    lines = []
    lines.append("  {")
    lines.append(f'    id: "{sw["id"]}",')
    lines.append(f'    name: "{sw["name"]}",')
    lines.append(f'    englishName: "{sw["englishName"]}",')
    lines.append(f'    category: "{sw["category"]}",')
    lines.append(f'    vendor: "{sw["vendor"]}",')
    lines.append(f'    description: "{sw["description"]}",')
    
    # features
    lines.append("    features: [")
    for f in sw["features"]:
        lines.append(f'      "{f}",')
    lines.append("    ],")
    
    # pricing
    lines.append("    pricing: [")
    for p in sw["pricing"]:
        lines.append("      {")
        lines.append(f'        plan: "{p["plan"]}",')
        lines.append(f'        price: "{p["price"]}",')
        lines.append("        features: [")
        for f in p["features"]:
            lines.append(f'          "{f}",')
        lines.append("        ],")
        lines.append("      },")
    lines.append("    ],")
    
    # pros
    lines.append("    pros: [")
    for p in sw["pros"]:
        lines.append(f'      "{p}",')
    lines.append("    ],")
    
    # cons
    lines.append("    cons: [")
    for c in sw["cons"]:
        lines.append(f'      "{c}",')
    lines.append("    ],")
    
    lines.append(f'    targetMarket: "{sw["targetMarket"]}",')
    lines.append(f'    arabicSupport: "{sw["arabicSupport"]}",')
    
    # saudi compliance
    sc_key = "saudiCompliance" if "saudiCompliance" in sw else "sudiCompliance"
    lines.append("    saudiCompliance: [")
    for s in sw.get(sc_key, []):
        lines.append(f'      "{s}",')
    lines.append("    ],")
    
    # integrations
    lines.append("    integrations: [")
    for i in sw["integrations"]:
        lines.append(f'      "{i}",')
    lines.append("    ],")
    
    # setup steps
    lines.append("    setupSteps: [")
    for s in sw["setupSteps"]:
        lines.append(f'      "{s}",')
    lines.append("    ],")
    
    # best practices
    lines.append("    bestPractices: [")
    for b in sw["bestPractices"]:
        lines.append(f'      "{b}",')
    lines.append("    ],")
    
    # training resources
    lines.append("    trainingResources: [")
    for t in sw["trainingResources"]:
        lines.append(f'      "{t}",')
    lines.append("    ],")
    
    lines.append("  },")
    return "\n".join(lines)


