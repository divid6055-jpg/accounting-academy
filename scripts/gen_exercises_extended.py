def serialize(ex):
    L = []
    L.append("  {")
    L.append(f'    id: "{ex["id"]}",')
    def esc(s):
        return s.replace('"', '\\"')
    L.append(f'    title: "{esc(ex["title"])}",')
    L.append(f'    description: "{esc(ex["description"])}",')
    L.append(f'    category: "{esc(ex["category"])}",')
    L.append(f'    difficulty: "{ex["difficulty"]}",')
    L.append(f'    question: "{esc(ex["question"])}",')
    L.append("    data: {")
    for k, v in ex["data"].items():
        if isinstance(v, str):
            L.append(f'      "{k}": "{esc(v)}",')
        elif isinstance(v, bool):
            L.append(f'      "{k}": {"true" if v else "false"},')
        else:
            L.append(f'      "{k}": {v},')
    L.append("    },")
    L.append(f'    hint: "{esc(ex["hint"])}",')
    L.append("    steps: [")
    for s in ex["steps"]:
        L.append(f'      "{esc(s)}",')
    L.append("    ],")
    L.append(f'    answer: "{esc(ex["answer"])}",')
    L.append(f'    explanation: "{esc(ex["explanation"])}",')
    L.append(f'    relatedTopic: "{esc(ex["relatedTopic"])}",')
    L.append(f'    timeAllocation: {ex["timeAllocation"]},')
    L.append(f'    learningObjective: "{esc(ex["learningObjective"])}",')
    L.append("  },")
    return L


