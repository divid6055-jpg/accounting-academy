def money(n):
    """Format with commas as thousand separators."""
    return f"{n:,}"

def num(low, high, avoid_round=True):
    """Generate a number that's not round (avoids multiples of 1000 when possible)."""
    for _ in range(20):
        v = random.randint(low, high)
        if not avoid_round:
            return v
        if v % 1000 != 0 and v % 500 != 0:
            return v
    return v

