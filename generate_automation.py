def format_automation_process(ap):
    """Format automation process entry as TypeScript"""
    lines = []
    lines.append("  {")
    lines.append(f'    id: "{ap["id"]}",')
    lines.append(f'    process: "{ap["process"]}",')
    lines.append(f'    description: "{ap["description"]}",')
    
    lines.append("    manualSteps: [")
    for s in ap["manualSteps"]:
        lines.append(f'      "{s}",')
    lines.append("    ],")
    
    lines.append("    automatedSteps: [")
    for s in ap["automatedSteps"]:
        lines.append(f'      "{s}",')
    lines.append("    ],")
    
    lines.append("    tools: [")
    for t in ap["tools"]:
        lines.append(f'      "{t}",')
    lines.append("    ],")
    
    lines.append("    benefits: [")
    for b in ap["benefits"]:
        lines.append(f'      "{b}",')
    lines.append("    ],")
    
    lines.append("    challenges: [")
    for c in ap["challenges"]:
        lines.append(f'      "{c}",')
    lines.append("    ],")
    
    lines.append("    implementation: [")
    for i in ap["implementation"]:
        lines.append(f'      "{i}",')
    lines.append("    ],")
    
    lines.append(f'    roi: "{ap["roi"]}",')
    lines.append(f'    example: "{ap["example"]}",')
    
    lines.append("  },")
    return "\n".join(lines)


