def format_digital_tool(tool):
    """Format digital tool entry as TypeScript"""
    lines = []
    lines.append("  {")
    lines.append(f'    id: "{tool["id"]}",')
    lines.append(f'    name: "{tool["name"]}",')
    lines.append(f'    category: "{tool["category"]}",')
    lines.append(f'    description: "{tool["description"]}",')
    
    lines.append("    useCases: [")
    for u in tool["useCases"]:
        lines.append(f'      "{u}",')
    lines.append("    ],")
    
    lines.append("    benefits: [")
    for b in tool["benefits"]:
        lines.append(f'      "{b}",')
    lines.append("    ],")
    
    lines.append("    limitations: [")
    for l in tool["limitations"]:
        lines.append(f'      "{l}",')
    lines.append("    ],")
    
    lines.append("    setupSteps: [")
    for s in tool["setupSteps"]:
        lines.append(f'      "{s}",')
    lines.append("    ],")
    
    lines.append("    bestPractices: [")
    for b in tool["bestPractices"]:
        lines.append(f'      "{b}",')
    lines.append("    ],")
    
    lines.append(f'    example: "{tool["example"]}",')
    
    lines.append("  },")
    return "\n".join(lines)


